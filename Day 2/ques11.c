#include<stdio.h>
void main()
{
int i,j,n,m;
scanf("%d",&n);
for(i=1;i<=n;i++)
{
if(i==n)
{
for(j=1;j<=n;j++)
{
printf("%d",j);
}
m=n;
for(j=1;j<=n;j++)
{
printf("%d",m);
m=m-1;
}
}
if(i<n)
{
for(j=1;j<=i;j++)
{
printf("%d",j);
}
for(j=1;j<=2*n-2*i;j++)
{
printf(" ");
}
m=i;
for(j=1;j<=i;j++)
{
printf("%d",m);
m=m-1;
}
}
printf("\n");
}
}