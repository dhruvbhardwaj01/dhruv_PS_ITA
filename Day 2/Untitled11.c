#include<stdio.h>
void main()
{
int i,j,n,m;
scanf("%d",&n);
for(i=1;i<=n;i++)
{
if(i==1)
{
for(j=1;j<=n;j++)
{
printf("%d\t",j);
}
for(j=1;j<=n-i;j++)
{
printf("%d\t",n-j);
}
}
if(i>1)
{
for(j=1;j<=n-i+1;j++)
{
printf("%d\t",j);
}
for(j=1;j<=2*(i-1)-1;j++)
{
printf("\t");
}
m=n-i+1;
for(j=1;j<=n-i+1;j++)
{
printf("%d\t",m);
m=m-1;
}
}
printf("\n");
}
}
